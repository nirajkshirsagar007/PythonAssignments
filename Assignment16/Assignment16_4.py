# 4. Write a program which display 5 times Marvellous on screen.
#   Output:
#   Marvellous
#   Marvellous
#   Marvellous
#   Marvellous
#   Marvellous

def Display(No1):
    for i in range(No1):
        print("Marvellous")
        
def main():
    Display(5)

if __name__ == "__main__":
    main()

